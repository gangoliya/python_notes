# =
x = 5
print(x)

# == (Comparison Operator)
print(x == 5)

# +=
x += 5 # x = x+5
print(x)  #10

# -=
x -= 5
print(x) #5


# *=
x *= 5
print(x) #25

# /=
x /= 5
print(x) #5


# %= (Remainder)
x = 9
x %= 5
print(x) #4


# //=
x //= 3
print(x) #1

# **=
x = 2
x **= 2
print(x) #4