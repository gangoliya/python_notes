x = 5
y = 3

# +
print(x+y)

# - 
print(x-y)

# *
print(x*y)

# /
print(x/y)

# % (modulus)
print(x%y)

# **(exponentiation)(Power of)
print(x**y)

# // (Floor of)
print(x//y)








a = 5 # int
b = 2.0 # float
c = 3j # complex
d = 's' # string
e = 3 # int


# INT (operator) INT returns INT
print(a*e)

# INT (operator) FLOAT returns FLOAT
print(a*b)

# INT (operator) COMPLEX returns COMPLEX
print(a*c)

# INT (operator) STRING returns ERROR
print(a*d) # 6 * '6'



# Exceptions
print(a/e) # INT / INT


print(a*d)  # INT * STRING


